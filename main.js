function person(name,age,weight,height) {
    document.write(`お名前は、${name}です。年齢は、${age}歳です。体重は、${weight}kgです。身長は、${height}cmです。<br>`);
}
// personの中はname = "高橋"
person("高橋",20,60,175);
person("鈴木",30,50,160);
person("佐藤",40,55,170);


